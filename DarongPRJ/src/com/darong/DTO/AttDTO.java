package com.darong.DTO;

public class AttDTO {
		private String attSeq;
		private String attNSeq;
		private String attTitle;
		private String attName;
		private String attDate;
		private String regUserNo;
		private String regDt;
		public String getAttSeq() {
			return attSeq;
		}
		public void setAttSeq(String attSeq) {
			this.attSeq = attSeq;
		}
		public String getAttNSeq() {
			return attNSeq;
		}
		public void setAttNSeq(String attNSeq) {
			this.attNSeq = attNSeq;
		}
		public String getAttTitle() {
			return attTitle;
		}
		public void setAttTitle(String attTitle) {
			this.attTitle = attTitle;
		}
		public String getAttName() {
			return attName;
		}
		public void setAttName(String attName) {
			this.attName = attName;
		}
		public String getAttDate() {
			return attDate;
		}
		public void setAttDate(String attDate) {
			this.attDate = attDate;
		}
		public String getRegUserNo() {
			return regUserNo;
		}
		public void setRegUserNo(String regUserNo) {
			this.regUserNo = regUserNo;
		}
		public String getRegDt() {
			return regDt;
		}
		public void setRegDt(String regDt) {
			this.regDt = regDt;
		}
}
